#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn navigate_debug_format() {
        let op = BackendOp::Navigate {
            target_id: "ABC123".into(),
            url: "https://example.com".into(),
        };
        let debug = format!("{op:?}");
        assert!(debug.contains("Navigate"));
        assert!(debug.contains("ABC123"));
    }

    #[test]
    fn clone_works() {
        let op = BackendOp::Evaluate {
            target_id: "T1".into(),
            expression: "1+1".into(),
            return_by_value: true,
        };
        let cloned = op.clone();
        match cloned {
            BackendOp::Evaluate {
                expression,
                return_by_value,
                ..
            } => {
                assert_eq!(expression, "1+1");
                assert!(return_by_value);
            }
            _ => panic!("wrong variant after clone"),
        }
    }

    #[test]
    fn dispatch_mouse_event_fields() {
        let op = BackendOp::DispatchMouseEvent {
            target_id: "T1".into(),
            event_type: "mousePressed".into(),
            x: 150.0,
            y: 300.0,
            button: "left".into(),
            click_count: 1,
        };
        match op {
            BackendOp::DispatchMouseEvent {
                x, y, click_count, ..
            } => {
                assert!((x - 150.0).abs() < f64::EPSILON);
                assert!((y - 300.0).abs() < f64::EPSILON);
                assert_eq!(click_count, 1);
            }
            _ => panic!("wrong variant"),
        }
    }

    #[test]
    fn create_target_with_window() {
        let op = BackendOp::CreateTarget {
            url: "about:blank".into(),
            window_id: Some(42),
            new_window: false,
        };
        match op {
            BackendOp::CreateTarget {
                url,
                window_id,
                new_window,
            } => {
                assert_eq!(url, "about:blank");
                assert_eq!(window_id, Some(42));
                assert!(!new_window);
            }
            _ => panic!("wrong variant"),
        }
    }

    #[test]
    fn target_id_returns_some_for_page_ops() {
        let op = BackendOp::Navigate {
            target_id: "T1".into(),
            url: "https://example.com".into(),
        };
        assert_eq!(op.target_id(), Some("T1"));
    }

    #[test]
    fn target_id_returns_none_for_browser_ops() {
        assert!(BackendOp::GetTargets.target_id().is_none());
        let op = BackendOp::CreateTarget {
            url: "about:blank".into(),
            window_id: None,
            new_window: false,
        };
        assert!(op.target_id().is_none());
    }

    #[test]
    fn is_page_scoped_true_for_page_commands() {
        let op = BackendOp::Evaluate {
            target_id: "T1".into(),
            expression: "1+1".into(),
            return_by_value: true,
        };
        assert!(op.is_page_scoped());
    }

    #[test]
    fn is_page_scoped_false_for_target_management() {
        assert!(!BackendOp::GetTargets.is_page_scoped());
        let op = BackendOp::CloseTarget {
            target_id: "T1".into(),
        };
        assert!(!op.is_page_scoped());
    }
}
